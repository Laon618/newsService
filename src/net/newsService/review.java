package net.newsService;

public class review {
	int rid;
	String body;
	String name;
	int nid;
	
	public int getRid() {
		return rid;
	}
	public String getBody() {
		return body;
	}
	public String getName() {
		return name;
	}
	public int getNid() {
		return nid;
	}
	public void setRid(int rid) {
		this.rid = rid;
	}
	public void setBody(String body) {
		this.body = body;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setNid(int nid) {
		this.nid = nid;
	}

}
